# This program does basic arithmetic math.


def main():
    print("10-5={}".format(10 - 5))
    print("2+4={}".format(2 + 4))
    print("8*2={}".format(8 * 2))
    print("6/3={}".format(6 / 3))
    print("1/4={}".format(1 / 4))
    print("2+5/3**2*5={}".format(2 + 5 / 3**2 * 5))
    print("2^3={}".format(2 ^ 3))  # bitwise operator
    print("2**3={}".format(2**3))  # exponent
    print("4**0.5={}".format(4**0.5))  # square root


if __name__ == "__main__":
    main()
