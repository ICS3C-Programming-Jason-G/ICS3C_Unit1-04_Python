# This program calculates the area & perimeter of a 15.3cm X 29.1cm rectangle.


def main():
    print("For a rectangle with length 29.1cm and width 15.3cm")
    print("")
    print("The area is {:.1f}cm^2".format(29.1 * 15.3))
    print("The perimeter is {:.1f}cm".format(2 * (29.1 + 15.3)))


if __name__ == "__main__":
    main()
